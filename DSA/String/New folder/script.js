// 4. Happy number 

let num = Number(prompt("Enter the number : "));

let set = new Set();

function happy(num){

    while(true){
        let sum = 0;

        while(num > 0){
            let rem = num % 10;
            sum += (rem * rem);
            num = Math.floor(num / 10);
        }

        if(sum === 1) return true;
        if(set.has(sum)) return false;
        else(set.add(sum))
        num = sum;
    }
}


console.log(happy(num))